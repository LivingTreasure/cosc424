
public class Token {
	private int token;
	private int attribute;
	
	public Token() {
		super();
	}
	
	public Token(int token, int attribute) {
		super();
		this.token = token;
		this.attribute = attribute;
		
	}
	
	public int getToken() {
		return token;
	}
	
	public int getAttribute() {
		return attribute;
	}

}
